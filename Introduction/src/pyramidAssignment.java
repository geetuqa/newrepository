
public class pyramidAssignment {

	public static void main(String[] args) {
		
		int k = 1;
		for(int i =0;i<4;i++)
		{
			//j=j+1;
		//	System.out.println(" outer loop started ");
			for(int il=1;il<=4-i;il++)
			{
				
				System.out.print(k);
				System.out.print("\t");
				k++;
			}
				
			System.out.println("");
		}

	}

}
